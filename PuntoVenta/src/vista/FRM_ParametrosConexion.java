    public Conexion obtenerInformacion(){
        Conexion temporal = null;
        temporal = new Conexion();
        temporal.setServidor(this.getServidor());
        temporal.setUsuario(this.getUsuario());
        temporal.setContrasenna(this.getContrasenna());
        temporal.setBaseDeDatos(this.getBaseDatos());
        
        return temporal;
    }

